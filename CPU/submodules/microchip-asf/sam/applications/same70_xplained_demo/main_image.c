void ili9488_draw_number(uint32_t ul_x, uint32_t ul_y, uint8_t index)
{
	uint32_t row, col, i;
	uint32_t offset;
	offset = ((uint32_t)index) * (48 * 48 / 8);
		
	for(row = 0; row < 6; row++) {
		for(col = 0; col < 48; col++) {
			for (i = 0; i < 8; i++) {
				if ((p_uc_numberset48x48[offset] >> i) & 0x1) {
					ili9488_draw_pixel(ul_x + col, ul_y + 8 * row + i);
				}
			}
			offset++;
		}
	}

}

void ili9488_draw_temperature(uint32_t ul_x, uint32_t ul_y, char* temperature)
{
	float temp_value_f = atof(temperature);
	int32_t temp_value_n;
	uint8_t index = 0;
	
	if(temp_value_f < 0.000001) {
		ili9488_draw_number(ul_x, ul_y, 11);
		index++;
		temp_value_n = (int32_t)(temp_value_f - 0.5);
		temp_value_n = -temp_value_n;
	} else {	
		temp_value_n = (int32_t)(temp_value_f + 0.5);
	}
	ul_x += 40;
	
	if(temp_value_n >= 10 && temp_value_n < 100) {
		ili9488_draw_number(ul_x, ul_y, temp_value_n/10);
		ul_x += 40;
		
		ili9488_draw_number(ul_x, ul_y, temp_value_n%10);
		ul_x += 40;
		
	} else {
		ili9488_draw_number(ul_x, ul_y, temp_value_n);
		ul_x += 40;
	}
	ili9488_draw_number(ul_x, ul_y, 10);	
}
